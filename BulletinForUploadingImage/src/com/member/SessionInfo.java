package com.member;

public class SessionInfo
{
	// 주요 속성 구성
	private String userName, userId;

	// getter / setter 구성
	public String getUserName()
	{
		return userName;
	}

	public void setUserName(String userName)
	{
		this.userName = userName;
	}

	public String getUserId()
	{
		return userId;
	}

	public void setUserId(String userId)
	{
		this.userId = userId;
	}
	
	
}
